import math


x = 9.9


# to get value of pi >>>math.pi
print(math.pi)

#to get value of exponential constant
print(math.e)

# to square root
result = math.sqrt(x)
print(result)

# to ceil round up to next ex :- 3.1 will be 4
print(math.ceil(x))

#to floor round down to previous ex :- 3.9 will be 3
print(math.floor(x))

